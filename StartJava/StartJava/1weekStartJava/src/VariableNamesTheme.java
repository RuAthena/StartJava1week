public class VariableNamesTheme {
    public static void main(String[] args) {
        // Разные переменные (это название блока)
        // Цифра
        int digit = 7;
        System.out.println("Цифра = " + digit);

        // Сумма чисел
        int sumDigits = 15;
        System.out.println("Сумма чисел = " + sumDigits);

        // Произведение цифр
        int productDigits = 12;
        System.out.println("Произведение цифр = " + productDigits);

        // Максимальное число
        int maxNumber = 100;
        System.out.println("Максимальное число = " + maxNumber);

        // Количество десятков
        int tensCount = 3;
        System.out.println("Количество десятков = " + tensCount);

        // Вес собаки
        int dogWeight = 20;
        System.out.println("Вес собаки = " + dogWeight);

        // Исходное число
        int initialNumber = 50;
        System.out.println("Исходное число = " + initialNumber);

        // Процент по вкладу
        int interestRate = 5;
        System.out.println("Процент по вкладу = " + interestRate);

        // Переменная хранит символ &
        char specialChar = '&';
        System.out.println("Переменная хранит символ = " + specialChar);

        // Код ошибки
        int errorCode = 404;
        System.out.println("Код ошибки = " + errorCode);

        // Тип сообщения
        String messageType = "информация";
        System.out.println("Тип сообщения = " + messageType);

        // Число нулей
        int numberOfZeros = 8;
        System.out.println("Число нулей = " + numberOfZeros);

        // Уникальное число
        int uniqueNumber = 123456;
        System.out.println("Уникальное число = " + uniqueNumber);

        // Случайное число
        int randomNumber = 42;
        System.out.println("Случайное число = " + randomNumber);

        // Математическое выражение
        int mathExpression = (5 + 3) * 2;
        System.out.println("Математическое выражение = " + mathExpression);

        // Счет в игре
        int gameScore = 1000;
        System.out.println("Счет в игре = " + gameScore);

        // Максимальная длина
        int maxLength = 255;
        System.out.println("Максимальная длина = " + maxLength);

        // Пункт меню
        int menuItem = 1;
        System.out.println("Пункт меню = " + menuItem);

        // Стоимость кофе
        int coffeePrice = 150;
        System.out.println("Стоимость кофе = " + coffeePrice);

        // Дата начала
        String startDate = "2023-01-01";
        System.out.println("Дата начала = " + startDate);

        // Окончание диапазона
        int rangeEnd = 200;
        System.out.println("Окончание диапазона = " + rangeEnd);

        // Полное имя работника месяца
        String employeeName = "Иван Иванов";
        System.out.println("Полное имя работника месяца = " + employeeName);

        // Заголовок электронной книги
        String eBookTitle = "Лучшие практики Java";
        System.out.println("Заголовок электронной книги = " + eBookTitle);

        // Размер
        int size = 512;
        System.out.println("Размер = " + size);

        // Вместимость
        int capacity = 100;
        System.out.println("Вместимость = " + capacity);

        // Счетчик
        int counter = 0;
        System.out.println("Счетчик = " + counter);

        // Путь до файла
        String filePath = "C:/documents/file.txt";
        System.out.println("Путь до файла = " + filePath);

        // Количество чисел в каждой строке
        int numbersPerLine = 5;
        System.out.println("Количество чисел в каждой строке = " + numbersPerLine);

        System.out.println(); // Пустая строка для разделения блоков

        // Boolean-переменные
        // Сотни равны?
        boolean hundredsEqual = true;
        System.out.println("Сотни равны? = " + hundredsEqual);

        // Компьютер включен?
        boolean computerOn = false;
        System.out.println("Компьютер включен? = " + computerOn);

        // Есть равные цифры?
        boolean hasEqualDigits = false;
        System.out.println("Есть равные цифры? = " + hasEqualDigits);

        // Создано?
        boolean created = true;
        System.out.println("Создано? = " + created);

        // Пустое?
        boolean isEmpty = false;
        System.out.println("Пустое? = " + isEmpty);

        // Активное?
        boolean isActive = true;
        System.out.println("Активное? = " + isActive);

        // Новое?
        boolean isNew = false;
        System.out.println("Новое? = " + isNew);

        // Электронная почта действительная?
        boolean isEmailValid = true;
        System.out.println("Электронная почта действительная? = " + isEmailValid);

        // Имеются уникальные строки?
        boolean hasUniqueStrings = false;
        System.out.println("Имеются уникальные строки? = " + hasUniqueStrings);

        System.out.println(); // Пустая строка для разделения блоков

        // Аббревиатуры
        // Старый universally unique identifier
        String oldUUID = "d3b07384d113edec49eaa623baf2b2c5";
        System.out.println("Старый UUID = " + oldUUID);

        // Производитель оперативной памяти
        String ramManufacturer = "Kingston";
        System.out.println("Производитель оперативной памяти = " + ramManufacturer);

        // Емкость жесткого диска
        int hddCapacity = 1024; // В ГБ
        System.out.println("Емкость жесткого диска = " + hddCapacity + " ГБ");

        // Протокол передачи гипертекста
        String httpProtocol = "HTTP/2";
        System.out.println("Протокол передачи гипертекста = " + httpProtocol);

        // Сокращенный uniform resource locator
        String shortUrl = "https://example.com";
        System.out.println("Сокращенный URL = " + shortUrl);

        // Новый идентификатор клиента
        String newClientID = "client_12345";
        System.out.println("Новый идентификатор клиента = " + newClientID);

        // American standard code for information interchange
        String asciiCode = "A";
        System.out.println("ASCII = " + asciiCode);
    }

    }

